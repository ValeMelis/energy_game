pub fn number_string(num: u128) -> String{
    let num: String = match num {
        0..=999 => format!("{}",num),
        1_000..=999_999 => format!("{}k",num/1000),
        1_000_000..=999_999_999 => format!("{}M",num/1_000_000),
        1_000_000_000..=999_999_999_999 => format!("{}G",num/1_000_000_000),
        1_000_000_000_000..=999_999_999_999_999 => format!("{}T",num/1_000_000_000_000),
        1_000_000_000_000_000..=999_999_999_999_999_999 => format!("{}P",num/1_000_000_000_000_000),
        1_000_000_000_000_000_000..=999_999_999_999_999_999_999 => format!("{}E",num/1_000_000_000_000_000_000),
        1_000_000_000_000_000_000_000..=999_999_999_999_999_999_999_999 => format!("{}Z",num/1_000_000_000_000_000_000_000),
        _ => format!("{}Y",num/1_000_000_000_000_000_000_000_000),
    };

    num
}